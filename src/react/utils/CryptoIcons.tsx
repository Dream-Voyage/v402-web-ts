/**
 * 获取网络对应的图标组件
 */
export const getNetworkIcon = (network: string): React.FC<IconProps> => {
    const networkLower = network.toLowerCase();

    if (networkLower.includes('solana')) {
        return SolanaIcon;
    }

    if (networkLower.includes('base')) {
        return BaseIcon;
    }

    // 默认返回 Base 图标（作为通用 EVM 图标）
    return BaseIcon;
};

/**
 * 获取货币对应的图标组件
 */
export const getCurrencyIcon = (currency: string): React.FC<IconProps> => {
    switch (currency.toUpperCase()) {
        case 'USDC':
            return USDCIcon;
        case 'SOL':
            return SolanaIcon;
        default:
            return USDCIcon;
    }
};

